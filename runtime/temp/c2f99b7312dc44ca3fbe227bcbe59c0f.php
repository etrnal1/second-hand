</p>
<p>
    <br />
</p>
<p>
    验机步骤：
</p>
<p>
    1、“抹掉所有内容”，点：设置——通用——还原——抹掉所有内容和设置！注意，别选错了，要选第2个“抹掉所有内容”的那个，而不是第1个还原所有设置！这样所有的靠越狱改序列号的翻新机器就会重启不能！（也就是白苹果）如果不做第一步，接下来的步骤都白做了。如果需要密码或网络，就让卖家给密码，找网络的地方，这一步千万不能省。如果不让还原，肯定心虚有诈，你就别买了。
</p>
<p>
    <br />
</p>
<p>
    2、判断是否是5改成5S 如果能成功抹掉并重启，那么接下来看：设置——指纹——新添加指纹，是否好用。 如果不能新增指纹，就是个伪装成5S的5，这种机器也算是翻新机改装机了，千万不能买。
</p>
<p>
    <br />
</p>
<p>
    3、点击：设置——通用——关于本机。首先看型号来辨别产地，例如 MD999CH/A，如果CH位置的两个字母是CH，那么是国行；如果是ZP，港行；最怕是J，日版有锁机，尤其是运营商那里是日文的。
</p>
<p>
    <br />
</p>
<p>
    4、查验保修期点击：设置——通用——关于本机。看序列号，然后到苹果官网输入验证。看保修日期是啥时候。是否如卖家所说。
</p>
<p>
    <br />
</p>
